    private void AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddActionPerformed
        // TODO add your handling code here:
        
          con=DBConnect.Connect();
        JL1.setVisible(false);
        JL2.setVisible(false);
        JL3.setVisible(false);
        JL4.setVisible(false);
        
        try{
                if(trim.getText().equals(""))
        {
            JL1.setVisible(true);
        }
       else if(C_code.getText().equals(""))
        {
            JL2.setVisible(true);
        }
        else if(C_title.getText().equals(""))
        {
            JL3.setVisible(true);
        }
        else if(C_hour.getText().equals(""))
        {
            JL4.setVisible(true);
        }
       
        
            else{
        
        
             String sql ="insert into Courses (trimester,Course_code,Course_title,Credit_Hr)values(?,?,?,?)";
             ps = (PreparedStatement)con.prepareStatement(sql);
                ps.setString(1,trim.getText());
                ps.setString(2,C_code.getText());
                ps.setString(3,C_title.getText());
                ps.setString(4,C_hour.getText());
                
                
                rs=(ResultSet)ps.executeQuery();
                 if(rs.next()){
                     
                    JOptionPane.showMessageDialog(null,"Course successfully added ! ");
                     
                 }
        
        
        }
                
        
        
        }catch(Exception e){
        JOptionPane.showMessageDialog(null,"Course already exist!");
        
        }
        
    }//GEN-LAST:event_AddActionPerformed

