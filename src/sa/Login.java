    private void LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginActionPerformed
        
    // Login will be performed for a valid user and open a new page while click Login button
        
        con=DBConnect.Connect();  //Connect with the database
        
        Jlbl1.setVisible(false);
        Jlbl2.setVisible(false);
        try{
       if(User.getText().equals(""))
        {
            Jlbl1.setVisible(true);
        }
       else if(Pass.getText().equals(""))
        {
            Jlbl2.setVisible(true);
        }
         else{
                String sql ="select *from Admin where Id=? and Password=?";  // SQL command to select a user fro Login
                
                
                ps = (PreparedStatement)con.prepareStatement(sql);
                ps.setString(1,User.getText());
                ps.setString(2,Pass.getText());
                rs=(ResultSet)ps.executeQuery();
                 if(rs.next()){    // if valied user is successfully selected then logiin success and go t new page
                     
                    Main m=new Main();
                    m.setVisible(true);
          
                    m.setLocationRelativeTo(this);
                    this.dispose();
                     
                 }
                 else{
                 JOptionPane.showMessageDialog(null,"Invalid User name or Password");   // show this message if user 
                                                                                         //is not valid or unregistered
                 }
                
                }
        
             }catch(Exception e){    // therow an exeption in case of error path an debug.
                 
             JOptionPane.showMessageDialog(null,e);
             
             }
       
        
        
    }//GEN-LAST:event_LoginActionPerformed

