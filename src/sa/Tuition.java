    private void AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddActionPerformed
        // TODO add your handling code here:
        con=DBConnect.Connect();
         JL1.setVisible(false);
         JL2.setVisible(false);
         JL3.setVisible(false);
         JL4.setVisible(false);
        
        try{
            if(ID.getText().equals("")){
            JL1.setVisible(true);
            }
            if(Date.getText().equals("")){
            JL2.setVisible(true);
            }
            if(PType.getText().equals("")){
            JL3.setVisible(true);
            }
            if(Fee.getText().equals("")){
            JL4.setVisible(true);
            }
            else{
            String sql="insert into Tuition(Id,Pay_date,Pay_Type,fee)values(?,?,?,?)";
            ps = (PreparedStatement)con.prepareStatement(sql);
            ps.setString(1,ID.getText());
            ps.setString(2,Date.getText());
            ps.setString(3,PType.getText());
            ps.setString(4,Fee.getText());
            rs=(ResultSet)ps.executeQuery();
            if(rs.next()){
            
            JOptionPane.showMessageDialog(null, "Succeccfully added!");
            
            }
            else{
             JOptionPane.showMessageDialog(null, "Something is missing!");
            
            }
            
            
            
            }
            
         
        
        
        
        }
        catch(Exception e){
        
        JOptionPane.showMessageDialog(null, "Something is missing!");
        
        }
        
        
        
        
    }//GEN-LAST:event_AddActionPerformed

